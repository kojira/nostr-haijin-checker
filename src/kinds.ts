/**
 * 取得・採点の対象とする Nostr イベント種別（kind）の許可リスト。
 *
 * heavy fetch（`nostr/query.ts`）・ストリーク（`nostr/streak.ts`）・採点
 * （`scoring/`）が **共通の既定**としてこの 1 か所を参照する。リレーへの問い合わせも、
 * 稼働日・継続性の判定も、すべてこの許可リストに含まれる kind だけを対象にする。
 *
 * 対象は「人の活動量」を表す次のカテゴリに限定する:
 *  - プロフィール … `0`（メタデータ）
 *  - 投稿 … `1`（テキストノート）
 *  - DM 関連 … `4`（NIP-04 暗号化 DM）/ `13`・`14`・`1059`（NIP-17/59 のシール・
 *    チャットメッセージ・ギフトラップ）
 *  - リポスト … `6`
 *  - リアクション … `7`
 *  - 公開チャンネル関連 … `40`〜`44`（チャンネル作成・メタデータ・発言・
 *    メッセージ非表示・ユーザーミュート: NIP-28）
 *
 * これら以外の kind（フォローリスト `3`、リレーリスト、各種置換可能イベントなど）は、
 * 密度・稼働日・継続性といった「活動量」の観測から **除外** する。
 *
 * 署名検証は行わない。リレーが返したイベントをそのまま信頼する（relay-trusted）。
 */
export const ALLOWED_KINDS: number[] = [
  0, // プロフィール（メタデータ）
  1, // テキスト投稿
  4, // 暗号化 DM（NIP-04）
  6, // リポスト
  7, // リアクション
  40, // 公開チャンネル作成（NIP-28）
  41, // 公開チャンネルメタデータ（NIP-28）
  42, // 公開チャンネルメッセージ（NIP-28）
  43, // メッセージ非表示（NIP-28）
  44, // ユーザーミュート（NIP-28）
  13, // シール（NIP-59）
  14, // チャットメッセージ（NIP-17 DM）
  1059, // ギフトラップ（NIP-59）
];

/** 許可リストに含まれる kind か（採点入力の防御的フィルタ等に使う）。 */
const ALLOWED_KIND_SET = new Set(ALLOWED_KINDS);
export function isAllowedKind(kind: number): boolean {
  return ALLOWED_KIND_SET.has(kind);
}
